package com.example.muhammadzubair.ema.model;

public class objectEvents
{
    private String eTitle;
    private String eDetails;
    private String eDate;
    private String eTime;
    private String eVenue;
    private String eGlink;
    private String eMlink;

    public objectEvents() {
    }

    public String getETitle() {
        return eTitle;
    }

    public void setETitle(String eTitle) {
        this.eTitle = eTitle;
    }

    public String getEDetails() {
        return eDetails;
    }

    public void setEDetails(String eDetails) { this.eDetails = eDetails; }

    public String getEDate() {
        return eDate;
    }

    public void setEDate(String eDate) {
        this.eDate = eDate;
    }

    public String getETime() {
        return eTime;
    }

    public void setETime(String eTime) { this.eTime = eTime; }

    public String getEVenue() {
        return eVenue;
    }

    public void setEVenue(String eVenue) { this.eVenue = eVenue; }

    public String getEGlink() {
        return eGlink;
    }

    public void setEGlink(String eGlink) { this.eGlink = eGlink; }

    public String getEMlink() {
        return eMlink;
    }

    public void setEMlink(String eMlink) { this.eMlink = eMlink; }


}
